package co.edu.uptc.demo2.models;

public class Person{
    private String code;
    private String name;
    private String lastName;
    private char gender;
    private char char1;
    private char char2;
    public Person(String code, String name, String lastName, char gender, char char1, char char2) {
        this.code = code;
        this.name = name;
        this.lastName = lastName;
        this.gender = gender;
        this.char1 = char1;
        this.char2 = char2;
    }

    

}